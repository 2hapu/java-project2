package com.example.javaproject.week2.day1;

public class TypeCasting2 {
    public static void main(String[] args) {

        // 실수형 변수 선언
        double dValue = 4.24;

        // 정수형 변수 선언
        int iValue = 10;

        // 정수형 -> 실수형으로 자동 형 변환
        double dResult = dValue + iValue;

        System.out.println(dResult); // 출력 결과 : 14.24
    }
}

