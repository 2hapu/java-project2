package com.example.javaproject.codeup;

import java.util.Scanner;

public class Codeup1073 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        while (true){
            int iValue = sc.nextInt();
            if(iValue == 0){
                break;
            }
            System.out.println(iValue);
        }
    }
}
